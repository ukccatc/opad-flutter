  static Map<String, dynamic>? findByEmail(String email) {
    try {
      return users.firstWhere(
        (user) =>
            user['Email'].toString().toLowerCase() ==
            email.toLowerCase().trim(),
      );
    } catch (e) {
      return null;
    }
  }

  /// Find user by ID
  static Map<String, dynamic>? findById(int id) {
    try {
      return users.firstWhere((user) => user['id'] == id);
    } catch (e) {
      return null;
    }
  }

  /// Find user by user_id (WordPress reference)
  static Map<String, dynamic>? findByUserId(int userId) {
    try {
      return users.firstWhere((user) => user['user_id'] == userId);
    } catch (e) {
      return null;
    }
  }

  /// Verify password (MD5 hash comparison)
  static bool verifyPassword(String email, String passwordHash) {
    final user = findByEmail(email);
    if (user == null) return false;
    return user['Password'] == passwordHash.toLowerCase();
